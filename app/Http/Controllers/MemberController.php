     */
    public function store(Request $request)
    {
        $member=new Member;

        $member->name=$request->input('name');
        $member->telephone=$request->input('telephone');
        $member->email=$request->input('email');
    
        $member->save();
    
        //一覧表示画面にリダイレクト
        return redirect('members');
    }

